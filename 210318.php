                                <p><b>На заседании Рабочей группы Национального совета по профессиональным квалификациям при Президенте России по вопросам оценки квалификации и качества
                                        подготовки кадров 14 марта эксперты и представители профессиональных сообществ обсудили направления дальнейшего развития Национальной системы
                                        квалификаций (НСК).</b></p>
                                <p>После принятия в июле 2016 года закона «О независимой оценке квалификации» №238-ФЗ в целом ряде отраслей создана необходимая инфраструктура
                                    для управления системой квалификаций, соответствующая требованиям реальной экономики. Большинство инструментов системы создаются в цифровом
                                    формате: это касается Реестра сведений о квалификации, программно-методических комплексов разработки стандартов, оценочных средств.
                                    Утверждены 1087 профстандартов, в реестр внесены 1256 квалификаций, для 655 из них разработаны тесты в цифровых форматах.
                                    30 советов по профессиональным квалификациям создали 194 центра оценки квалификаций в 51 регионе и в 19 сферах деятельности.
                                    Через сдачу профессиональных экзаменов прошли более 9600 человек, 93% из них получили свидетельства о квалификации.</p>
                                <p>«Мы вступили в новую стадию развития, когда перешли к массовому внедрению ранее предложенных наработок и идей. Внедрение, как бывает часто,
                                    идет непросто», — заявил <b>Александр Лейбович</b>, генеральный директор Национального агентства развития квалификаций. По его словам,
                                    Национальная система квалификаций требует новых решений, которые позволят двигаться дальше. Нет механизмов замены старых квалификаций на новые,
                                    по-прежнему действуют старые системы оценки квалификации на предприятиях и в учебных заведениях. «Фактически мы живем в двух системах — старой и новой.
                                    Должно быть правилом: вводишь новый профессиональный стандарт — убери с рынка старые. С этим согласны и Минтруда РФ, и профсоюзы.
                                    Надо дальше обсуждать этот вопрос с Минюстом РФ», — сообщил глава Национального агентства.</p>
