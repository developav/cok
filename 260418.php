                                    стимулы введены для предприятий, участвующих в сетевых образовательных программах. Но применение этих стимулов обставлено многими барьерами. Например, завышены лицензионные
                                    требования, требуются библиотеки и медкабинеты. Это излишне, для бизнеса нужно ввести налоговые преференции</p>
