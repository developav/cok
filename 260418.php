                                    для бизнеса. По окончании обучения не по специальности, по данным Росстата, работают 31,3 процента выпускников вузов и 40,5 процента выпускников
                                    организаций среднего профессионального образования (СПО).<br>
                                    Сегодня высокий формальный образовательный потенциал населения не капитализируется в полной мере. В группе стран с высоким охватом населения
                                    высшим и средним профессиональным образованием у России самый низкий подушевой ВВП. Образование на треть - а в некоторых секторах на две
                                    трети - не соответствует запросам рынка труда.
