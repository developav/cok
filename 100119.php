                                        Казани, Ростове-на-Дону, Ханты-Мансийске, Екатеринбурге, Красноярске, Новосибирске, Салавате, Владивостоке, Воронеже), ресурсное обеспечение которых позволяет проводить профессиональные
                                        экзамены более чем по 100 квалификациям специалистов в областях наноэлектроники, оптоэлектроники и фотоники, новых материалов и покрытий, метрологии, стандартизации и безопасности
                                        инновационной продукции наноиндустрии; </li>
