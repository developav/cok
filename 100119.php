                                <p>Программа «Развитие системы оценки профессиональных квалификаций в наноиндустрии на период 2016-2018 годов» была принята в 2015 году. Управление
                                    Программой было возложено на «Межотраслевое объединение наноиндустрии» – базовую организацию СПК в наноиндустрии. </p>
