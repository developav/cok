                                    Программа предусматривала создание центров оценки квалификаций; разработку оценочных средств для процедур оценки; формирование пула подготовленных
                                    экспертов по оценке квалификаций и запуск всей системы. </p>
                                <p>За три года реализации Программы была сформирована инфраструктура системы независимой оценки квалификаций в наноиндустрии, в том числе: </p>
