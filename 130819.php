                                <p>Мало кто знает, но с июля 2019 года все работники нашей страны, а также те, кто еще только учится, живут в новом мире. Работодателю (правда, пока речь идет всего о двух с половиной десятках профессий, но лиха беда начало) недостаточно только диплома о среднем или высшем профобразовании. Теперь требуется и подтверждение квалификации принимаемого на работу, а говоря проще — умения применять свои знания на практике. Первыми почувствовали новшество выпускники колледжей: в этом году многие из них прошли государственную итоговую аттестацию, совмещенную с независимой оценкой квалификации, и 63% свою квалификацию сумели подтвердить. Этих ребят возьмут на работу в первую очередь.</p>
                                <p>Суть новшества проста: диплом о высшем или среднем профессиональном образовании отныне подтверждает лишь уровень обучения работника. А вот его профессиональную квалификацию, позволяющую оценить, чего стоит сотрудник на деле, будут подтверждать независимые органы — центры оценки квалификации.</p>
                                <p>Пока независимая оценка существует лишь примерно в 25 отраслях, главным образом, связанных с жизнью и здоровьем наших граждан — здравоохранении, транспорте, включая воздушный, образовании и т.п. Но если раньше отраслевики оценивали эти квалификации по-своему и выдавали свои собственные сертификаты о квалификации, то теперь эта оценка станет независимой, а значит, и более точной, считают в Национальном агентстве развития квалификаций (НАРК). Ну а затем этот опыт распространится и на другие сферы экономики, отмечают в ведомстве.</p>
                                <p>Впрочем, «движение вширь» уже пошло. В рамках пилотного проекта «Профессиональный экзамен для студентов колледжей» выпускники программ СПО уже прошли государственную итоговую аттестацию, совмещенную с независимой оценкой квалификации. И этим летом подтвердить свою квалификацию в среднем сумело 63% выпуска, тогда как в колледжах, тесно сотрудничающих с работодателями, этот показатель практически достиг 100%.</p>
