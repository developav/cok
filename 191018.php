                                <p>«Развитие института «Национальной системы квалификаций», в том числе за счет повышения вовлеченности работодателей в совершенствование, внедрение и использование системы квалификаций», - говорится в документе
                                    «Основные направления деятельности Правительства Российской Федерации на период до 2024 года», утвержденного Председателем Правительства Российской Федерации Дмитрием Медведевым. </p>
