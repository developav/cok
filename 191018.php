                                    и социально-экономического развития.</p>
                                <p>По направлению «поддержка занятости и повышение эффективности рынка труда для обеспечения роста производительности труда» Правительством будут решены следующие задачи: формирование системы подготовки кадров,
                                    направленной на обучение основам повышения производительности труда, поддержка занятости населения в связи с реализацией мероприятий по повышению производительности труда на предприятиях.  Обеспечение
                                    развития инфраструктуры занятости и внедрения организационных и технологических инноваций с использованием цифровых и платформенных решений в целях поддержки уровня занятости населения и содействия в
                                    удовлетворении дополнительной потребности предприятий в кадровых ресурсах. </p>
                                <p>В части обеспечения подготовки высококвалифицированных кадров для цифровой экономики, будет организована поддержка талантливых школьников и студентов в области математики и информатики, способствующая
                                    обеспечению перспективного кадрового потенциала для решения задач развития цифровой экономики.
                                <p>Также, согласно документу, будет оказано содействие гражданам в освоении цифровой грамотности и компетенций цифровой экономики, в том числе предусматривающее обучение 10 млн. человек по онлайн-программам
                                    развития цифровой грамотности и предоставление 1 млн. цифровых сертификатов для обучения граждан по развитию компетенций цифровой экономики.</p>
