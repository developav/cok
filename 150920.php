                                    представители бизнеса и вузов страны. В ходе конференции также принимались вопросы в режиме-онлайн.</p>
                                <p>Одной из первых стала тема взаимодействия образовательных учреждений, центров оценки квалификаций и предприятий.
                                    По словам Аллы Факторович, независимая оценка квалификаций – это обратная связь от работодателей и оценка уровня подготовленности выпускников.
                                    «Это не оценка ради оценки, а инструмент развития, потому что анализ результатов профэкзаменов позволяет по-другому посмотреть и на содержание образования,
                                    и на образовательные технологии. Фактически перестроить образовательный процесс таким образом,
                                    чтобы результаты обучения соответствовали ожиданиям и требованиям работодателей».</p>
                                <p>Также Факторович пояснила, что команда Минпросвещения заинтересовалась результатами пилотного проекта в СПО и сейчас,
                                    в том числе и в рамках поручений Президента, формируется рабочая группа.</p>
                                <p>О том, что в рамках проведения оценки квалификаций студентов, в частности, в сфере наноиндустрии,
                                    к ним действительно применяются высокие стандарты подчеркнула и Ольга Крюкова: «Мы работаем на формирующемся рынке,
                                    на рынке высоких технологий и у нас постоянно меняется и трансформируется спрос на кадры, спрос, с которым мы выходим в сторону системы высшего образования.
                                    К тому же мы работаем с инженерными кадрами и у нас очень высокий уровень квалификации. Если брать срез наших квалификаций, а их сейчас разработано более 200,
                                    то большинство приходится на 6 и 7 уровень, а есть квалификаций 8 уровня, которые, как правило, требуют наличия опыта профессиональной деятельности».
                                    Эксперт пояснила, что в данном направлении еще будет проведена работа. К примеру, одним из решений является разработка входных квалификаций для студентов.</p>
                                <p>Секретарь СПК в наноиндустрии Сергей Ионов заявил, что они тесно обращаются в вузы страны с предложением внедрять ряд систем:
                                    «Причём в рамках программы у нас даже предусмотрены средства и мы готовы оплачивать работу экспертов,
                                    чтобы команда могла отработать формат и порядок проведения процедуры оценки квалификации. На наш взгляд,
                                    профессиональная общественная аккредитация – это диалог между вузом и предприятием. Мы здесь должны учитывать пожелания и работодателей, и вузов.
                                    Мы ежегодно по результатам общественной аккредитации меняем положения, исходя из современных реалий: где-то критерии подправляем, где-то требования».</p>
