                                    обсудили 14 сентября в технопарке «Идея» в рамках онлайн-сессии «Независимая оценка в образовании. Перспективы и барьеры».
                                    В мероприятии приняли участие эксперты из разных отраслей и регионов страны. Среди них – заместитель генерального директора
                                    Национального агентства развития квалификаций Алла Факторович, директор делового полимерного парка «Ком-Пласт» Сергей Цыбуков,
