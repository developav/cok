                                        высокотехнологичных отраслях 3.0»</b>, организованная Фондом инфраструктурных и образовательных программ и базовой организацией Совета по профессиональным
                                    квалификациям в наноиндустрии НП «Межотраслевое объединение наноиндустрии».</p>
                                <p>АО «НИИМЭ» на мероприятии представляла <b>зам.генерального директора по орг.развитию и управлению персоналом- руководитель Центра оценки квалификаций АО «НИИМЭ»
                                        Лилиана Поликарпова</b>.</p>

